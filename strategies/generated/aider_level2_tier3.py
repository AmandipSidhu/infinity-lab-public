"""Aider Validation Ladder — Level 2, Tier 3.

Generated by Aider from Tier 3 prompt:
  Previous backtest returned Sharpe ~0.55.
  Issue: Minute-bar crossovers fire too frequently; brief EMA/VWAP
         intersections cause whipsaw trades that erode returns via
         transaction costs. Position state tracking insufficient.
  Adjustment: Track previous signal state; only trade on confirmed
              crossovers (signal change vs. prior bar); add entry
              price tracking to avoid re-entry at same level.
  Rebuild targeting Sharpe 1.0±0.5.

Improvements over Tier 2:
  + Signal-change detection (crossover, not just level comparison)
  + _invested flag with explicit state machine
  + Entry price stored to support future stop-loss extension
"""

try:
    from AlgorithmImports import *  # noqa: F401,F403
except ImportError:
    pass  # Running outside QuantConnect LEAN environment (local analysis)


class VWAPEMACrossoverTier3(QCAlgorithm):
    """VWAP/EMA crossover with state tracking — Tier 3 (Aider iteration 3)."""

    def Initialize(self) -> None:
        self.SetStartDate(2022, 1, 1)
        self.SetEndDate(2023, 1, 1)
        self.SetCash(10000)

        equity = self.AddEquity("SPY", Resolution.Minute)
        self._symbol = equity.Symbol

        self._ema = self.EMA(self._symbol, 30, Resolution.Minute)
        self._vwap = self.VWAP(self._symbol, 30)

        self.SetWarmUp(30, Resolution.Minute)

        # State tracking
        self._prev_signal: int = 0   # 1 = ema>vwap, -1 = ema<vwap, 0 = unknown
        self._entry_price: float | None = None

    def OnData(self, data: Slice) -> None:
        if self.IsWarmingUp:
            return
        if not self._ema.IsReady or not self._vwap.IsReady:
            return

        ema_val = self._ema.Current.Value
        vwap_val = self._vwap.Current.Value

        current_signal = 1 if ema_val > vwap_val else -1
        invested = self.Portfolio[self._symbol].Invested

        # Only act on signal changes (confirmed crossover)
        if current_signal != self._prev_signal:
            if current_signal == 1 and not invested:
                self.SetHoldings(self._symbol, 1.0)
                self._entry_price = self.Securities[self._symbol].Price
                self.Debug(
                    f"[Tier3] BUY  EMA={ema_val:.4f} VWAP={vwap_val:.4f} @ {self.Time}"
                )
            elif current_signal == -1 and invested:
                self.Liquidate(self._symbol)
                self._entry_price = None
                self.Debug(
                    f"[Tier3] SELL EMA={ema_val:.4f} VWAP={vwap_val:.4f} @ {self.Time}"
                )

        self._prev_signal = current_signal
